    <h2>Procesos Cognitivos en la Niñez Media</h2>
    <small class="muted-text"><i>Erika Ortiz - Mg. en Educación</i></small>
    <br><br>
    Para el <a href="https://www.mineducacion.gov.co/1759/articles356787_recurso_1.pdf" target="_blank">Ministerio
        de Educación Nacional (2021)</a>, el período de la niñez media
    escolar va de los <b>6 a los 11 años </b>y se caracteriza por los avances frente al manejo de su
    cuerpo, la habilidad para la lectura, la escritura, la aritmética y para comprender su mundo
    desde la lógica.
    <br><br>
    <img src="https://aprendizajear.ludotk.com/images/aprendizaje-auto.png" class="img-fluid" style="float: left;" alt="">
    Desde lo <b>cognitivo </b>se amplía el vocabulario para atender y entender con
    una mayor capacidad de atención y concentración; en esta etapa los infantes tienen las
    herramientas cognitivas para reconocer símbolos, resolver problemas, tomar decisiones,
    anticiparse a sucesos diarios y establecer y alcanzar metas.
    <br><br>

    Comprender la mente humana has sido uno de los grandes desafíos de la
    psicología y la pedagogía, así como descubrir cómo se <b>aprende, cómo se piensa y cómo
        se acerca al conocimiento</b>, es por esto que para hablar de procesos cognitivos en la niñez
    media, es necesario abordar las diferentes teorías y posturas que se han desarrollado
    desde estas disciplinas, para lo cual, se revisan los enfoques piagetianos desde las
    operaciones concretas, el enfoque de procesamiento de la información relacionado con la
    atención, la planificación y la memoria y el enfoque psicométrico para abordar el tema de
    inteligencia que está directamente relacionado con el aprendizaje.
    <br><br>

    De acuerdo con <b>Piaget</b> hacia los siete años se inicia el periodo de operaciones
    concretas y continúa hasta los doce años aproximadamente. Según <b>Papalia y Matorrel</b>
    (2017), el pensamiento en esta edad se caracteriza por razonamientos lógicos en una
    situación concreta y real y pueden tener en cuenta diferentes aspectos en una situación.
    En esta edad las relaciones espaciales y de causalidad toman gran relevancia desde la
    experiencia, así como las <b>habilidades de categorización, seriación, interferencia transitiva,
        la inclusión de clase, el razonamiento deductivo e inductivo y el pensamiento espacial</b>.

    <br><br>

    El proceso de crecimiento evidencia avances en los procesos escolares y así
    también avanza su habilidad para regularse, desde el procesamiento de la información y
    la capacidad de concentrarse, planificar y autoevaluar su conducta, a esto es lo que
    Papalia y Matorrel (2017) llaman <b>funciones ejecutivas</b>, es decir la <b>conciencia y auto control
        de lo que sienten, piensan y cómo actúan </b>frente a estos para resolver problemas y
    alcanzar lo que se proponen.

    <br><br>

    Para <a href="https://revistas.comillas.edu/index.php/pensamiento/article/download/4691/4502" target="_blank">
        Martínez – Freire (2014), </a>la cognición hace referencia
    a los procesos mediante los cuales la información que entra al cerebro a través de los
    sentidos, se transforma, se almacena y se reutiliza en nuevas experiencias sensoriales,
    es decir, son los <b>procesos mentales que tienen lugar en la entrada de estímulos al
    sistema nervioso</b>, para lo cual plantea unas etapas que pueden agruparse en procesos
    cognitivos simples y procesos cognitivos superiores. Los primeros relacionados con el
    <b>sentir, percibir, atender, concentrase y recordar</b>; y los procesos cognitivos superiores
    relacionados con lenguaje y pensamiento, son fundamentales en todas las etapas
    escolares, especialmente en la niñez (Escobar-Gutiérrez, et. al, 2022).
    
    <br><br>
    
    <h4>Enfoque de procesamiento de la información</h4> 
    Plantea que las funciones
    ejecutivas promueven la conciencia en <b>pensamiento y comportamiento</b>, las cuales son
    fundamentales para su desarrollo integral y están directamente relacionadas con el
    <b>desarrollo físico y los factores ambientales</b>. En este enfoque se plantea la atención
    selectiva como la capacidad del estudiante de concentrarse por más tiempo en la
    información que necesita, es decir <b>la capacidad para elegir de manera autónoma la
    atención sin distraerse</b>, este aumento de atención selectiva se relaciona directamente con
    el control inhibitorio, es la habilidad de voluntariamente detener las respuestas que no se
    esperan. 
    
    <br><br>

    Papalia y Matreel (2017) también citan que en este enfoque <b>la memoria como
    elemento importantes en la niñez media</b>, definiendo la memoria de trabajo como el
    almacenaje corto de los estímulos sensoriales, aspecto que tiene una gran importancia
    en el rendimiento académico pues es la base para la estructura de aprendizajes nuevos;
    la <b>metamemoria</b> es la conciencia de practicar y aplicar estrategias para recordar; y la
    <b>mnemotecnia</b> se refiere a estrategias para recordar, aprender por asociación y establecer
    claves para lograrlo.

    <br><br>

    <h4>El enfoque psicométrico </h4>
    Está directamente relacionado con <b>la evaluación de la
    inteligencia</b>, concepto que actualmente ha sido replanteado desde diferentes que buscan
    definir qué es ser inteligente, más allá de la memorización de la información, de saber
    matemáticas o tener una habilidad especial por las ciencias. Una crítica reconocida e
    importante frente a este concepto y a las tradicionales pruebas de coeficiente intelectual
    es <b>la teoría de las inteligencias múltiples</b> que se orienta al éxito para la vida y no en el
    éxito en la escuela, lleva el concepto de inteligencia más allá de lo académico o del
    cumplimiento de estándares. 

    <img src="https://aprendizajear.ludotk.com/images/aprendizaje-autor.png" class="img-fluid" style="float: right;" alt="">
    
    <br><br>

    Para Gardner (1995), las tradicionales pruebas de
    inteligencia solo contemplan lo <b>lingüístico y la lógica matemática</b>, mientras que el plantea
    una serie de inteligencias más, que se pueden complementar entre sí, citando <b>la musical,
    la corporal kinestésica, la interpersonal, la intrapersonal, la naturalista y la espacial.</b>
    Los procesos cognitivos básicos también son conocidos como los dispositivos
    básicos de aprendizaje (DBA), según Azcoaga (2008), son los procesos que se requieren
    como determinantes para alcanzar el aprendizaje, se desarrollan de forma individual y
    pueden fortalecerse a través de adaptaciones del entorno afectando de forma positiva los
    resultados académicos. 
    
    <br><br>


    Son comúnmente conocidos la atención, memoria, concentración
    y sensopercepción. Para Martínez – Suárez, los procesos cognitivos básicos
    o dispositivos básicos de aprendizaje han sido clasificados en seis grupos: <b>motivación</b>,
    relacionada con el estado óptimo para aprender a través de estímulos que sean
    llamativos; <b>la atención fásica y tónica</b>, como un fenómeno que se relaciona con la
    orientación, la <b>estimulación sensorial</b> y el mantenimiento de receptividad a través de los
    canales sensoriales; <b>la habituación</b> que se relaciona con la atenuación de respuestas
    frente a estímulos repetitivos, <b>la memoria</b> frente a la capacidad de recordar a largo y
    mediano plazo y la sensopercepción como un proceso integral que se da desde el sistema
    nervioso y que genera respuestas de forma integral. 
    
    <br><br>

    Según Gallardo (2009), los procesos
    cognitivos básicos comprenden tres fases, la primera donde <b>el estudiante se acerca al
    proceso</b> y reconoce los pasos a seguir, la segunda donde <b>establece asociaciones</b> y la
    tercera donde el proceso se <b>perfecciona y automatiza</b>.
    Para Klimenko (2009), la educación primaria es el periodo de la educación de
    mayor influencia alrededor de la formación en la vida, de manera que es el momento en
    que se pone a prueba el bagaje evolutivo que cada niño que trae del preescolar, hay una
    <b>mayor exigencia frente a las habilidades cognitivas y metacognitivas</b>, por lo cual es
    necesario incluirlas en el proceso formativo de forma intencional y programada para
    alcanzar el aprendizaje autónomo y autodirigido. 
    
    <br><br>

    La metacognición se relaciona en esta
    edad al conocimiento que el estudiante tiene de su propia actividad cognitiva y el
    conocimiento de las estrategias que puede utilizar para ciertas tareas, pero también en el
    control de esta actividad, es decir como planifica, supervisa y evalúa de forma autónoma
    los resultados de la tarea, aspectos muy necesarios para aprender teniendo en cuenta
    que el estudiante emplea la metacognición para autorregular su aprendizaje y a la vez
    este proceso de autorregulación le permite acceder y procesar nuevos conocimientos

